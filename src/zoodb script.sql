-- -----------------------------------------------------
-- Schema zoo_db
-- -----------------------------------------------------
DROP SCHEMA IF EXISTS zoo_db;
CREATE SCHEMA IF NOT EXISTS zoo_db;
USE zoo_db;

-- -----------------------------------------------------
-- Table enclosure
-- -----------------------------------------------------
DROP TABLE IF EXISTS enclosure;
CREATE TABLE IF NOT EXISTS enclosure (
	enc_id			INT(4),
	enc_type		VARCHAR(45) NOT NULL,
    enc_price       DECIMAL(5,2),
    cleaned         DATE,
    PRIMARY KEY		(enc_id)
);

-- -----------------------------------------------------
-- Table animal
-- -----------------------------------------------------
DROP TABLE IF EXISTS animal;
CREATE TABLE IF NOT EXISTS animal (
	anim_id			INT(4),
    anim_name		VARCHAR(45),
    anim_type		VARCHAR(45),
    age				INT(3),
    diet			VARCHAR(45),
    fed             DATE,
    vetted          DATE,
    enc_id			INT(4),
    notes           VARCHAR(45),
    PRIMARY KEY		(anim_id),
    FOREIGN KEY		(enc_id)
		REFERENCES	enclosure(enc_id)
);

-- -----------------------------------------------------
-- Table ticket
-- -----------------------------------------------------
DROP TABLE IF EXISTS ticket;
CREATE TABLE IF NOT EXISTS ticket (
	tick_id			INT(4) AUTO_INCREMENT,
    pass_type		VARCHAR(45),
    price			DECIMAL(5,2),
    p_date			DATE,
    enc_id			INT(4),
    PRIMARY KEY		(tick_id),
    FOREIGN KEY		(enc_id)
		REFERENCES	enclosure(enc_id)
);

ALTER TABLE ticket AUTO_INCREMENT=20000;

-- -----------------------------------------------------
-- Table employee
-- -----------------------------------------------------
DROP TABLE IF EXISTS employee;
CREATE TABLE IF NOT EXISTS employee (
	emp_id			INT(4),
	emp_name        VARCHAR(45),
    salary			DECIMAL(10,2),
    dob				DATE,
    age				INT(3),
    job				VARCHAR(45),
    enc_id			INT(4),
    PRIMARY KEY		(emp_id),
    FOREIGN KEY		(enc_id)
		REFERENCES	enclosure(enc_id)
);

